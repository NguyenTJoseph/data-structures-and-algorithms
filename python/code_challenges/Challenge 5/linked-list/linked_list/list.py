class Node:
    def __init__(self, value, next=None):
        self.value = value
        self.next = next

class LinkedList:
    def __init__(self):
        self.head = None

    def insert(self, value):
        node = Node(value)
        node.next = self.head
        self.head = node

    def includes(self, value):
        current = self.head
        while current:
            if current.value == value:
                return True
            current = current.next
        return False

    def toString(self):
        current = self.head
        string = ""
        while current:
            string += "{ " + current.value + " } -> "
            current = current.next

        string += "NULL"
        return string
